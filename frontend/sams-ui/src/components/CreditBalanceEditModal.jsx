import React, { useState, useEffect, useRef } from 'react';
import { useClient } from '../context/ClientContext';
import { useAuth } from '../context/AuthContext';
import { updateCreditBalance, addCreditHistoryEntry } from '../api/hoaDuesService';
import { isSuperAdmin, isAdmin } from '../utils/userRoles';
import { getMexicoDateString } from '../utils/timezone';
import { formatAsMXN } from '../utils/hoaDuesUtils';
import { getFiscalYear } from '../utils/fiscalYearUtils';
import { getMexicoDate } from '../utils/timezone';
import { useDesktopStrings } from '../hooks/useDesktopStrings';
import { getAutogeneratedUserMessage } from '@shared/utils/creditUserMessage.js';
import { handleDecimalAmountChange, parseDecimalInput } from '../utils/decimalAmountInput.js';
import '../styles/SandylandModalTheme.css';
import '../styles/CreditBalanceModals.css';

function inferCreditEntryType({ isAmountMode, mode, difference }) {
  if (isAmountMode) {
    return mode === 'add' ? 'credit_added' : 'credit_used';
  }
  return difference >= 0 ? 'credit_added' : 'credit_used';
}

function CreditBalanceEditModal({ isOpen, onClose, unitId, currentBalance, year, onUpdate, mode }) {
  const { selectedClient } = useClient();
  const { samsUser } = useAuth();
  const { t } = useDesktopStrings();
  
  // mode can be 'add', 'remove', or undefined (for edit/new balance mode)
  const isAmountMode = mode === 'add' || mode === 'remove';
  
  // Debug logging when mode changes
  React.useEffect(() => {
    if (isOpen) {
      console.log('[CREDIT EDIT MODAL] Modal opened:', { mode, isAmountMode, currentBalance });
    }
  }, [isOpen, mode, isAmountMode, currentBalance]);
  
  const [newBalance, setNewBalance] = useState('');
  const [amount, setAmount] = useState(''); // For add/remove mode
  const [entryDate, setEntryDate] = useState(getMexicoDateString());
  const [notes, setNotes] = useState('');
  const [userMessage, setUserMessage] = useState('');
  const [source, setSource] = useState('admin');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);
  const userMessageTouchedRef = useRef(false);

  // Check if user has permission to edit credit balance
  const canEdit = isSuperAdmin(samsUser) || isAdmin(samsUser, selectedClient?.id);

  // Initialize form when modal opens
  useEffect(() => {
    if (isOpen) {
      if (isAmountMode) {
        // Amount mode: clear amount, set balance to current
        setAmount('');
        setNewBalance(currentBalance?.toString() || '0');
      } else {
        // Balance mode: set new balance to current balance initially
        setNewBalance(currentBalance?.toString() || '0');
        setAmount('');
      }
      setEntryDate(getMexicoDateString());
      setNotes('');
      setUserMessage(getAutogeneratedUserMessage({ source: 'admin', type: 'credit_added' }));
      setSource('admin');
      userMessageTouchedRef.current = false;
      setError(null);
    }
  }, [isOpen, currentBalance, isAmountMode]);

  const amountValueForPreview = parseDecimalInput(amount) || 0;
  const currentBalanceValueForPreview = parseFloat(currentBalance) || 0;
  const previewDifference = isAmountMode
    ? (mode === 'add' ? amountValueForPreview : -amountValueForPreview)
    : (parseFloat(newBalance) || currentBalanceValueForPreview) - currentBalanceValueForPreview;
  const previewSource = isAmountMode ? source : 'admin';
  const previewType = inferCreditEntryType({
    isAmountMode,
    mode,
    difference: previewDifference
  });

  useEffect(() => {
    if (!isOpen || userMessageTouchedRef.current) {
      return;
    }
    setUserMessage(getAutogeneratedUserMessage({ source: previewSource, type: previewType }));
  }, [isOpen, previewSource, previewType, userMessageTouchedRef]);

  const handleSave = async () => {
    if (!canEdit) {
      setError(t('creditEdit.errorNoPermission'));
      return;
    }

    let newBalanceValue;
    
    if (isAmountMode) {
      // Amount mode: validate amount and calculate new balance
      // Ensure we're reading the latest amount value and trimming whitespace
      const amountStr = amount ? amount.toString().trim() : '';
      const amountValue = parseDecimalInput(amountStr);
      
      console.log('[CREDIT EDIT MODAL] Amount mode save:', { 
        mode, 
        amount, 
        amountStr, 
        amountValue, 
        currentBalance,
        isNaN: isNaN(amountValue),
        isZero: amountValue === 0
      });
      
      if (!amountStr || isNaN(amountValue) || amountValue === 0) {
        setError(t('creditEdit.errorNonZeroAmount'));
        return;
      }
      
      // Ensure currentBalance is parsed as a number
      const currentBalanceValue = parseFloat(currentBalance) || 0;
      
      if (mode === 'add') {
        newBalanceValue = currentBalanceValue + amountValue;
        console.log('[CREDIT EDIT MODAL] Add calculation:', { currentBalanceValue, amountValue, newBalanceValue });
      } else { // mode === 'remove'
        newBalanceValue = currentBalanceValue - amountValue;
        console.log('[CREDIT EDIT MODAL] Remove calculation:', { 
          currentBalanceValue, 
          amountValue, 
          newBalanceValue,
          calculation: `${currentBalanceValue} - ${amountValue} = ${newBalanceValue}`
        });
      }
    } else {
      // Balance mode: validate new balance
      newBalanceValue = parseDecimalInput(newBalance);
      if (isNaN(newBalanceValue)) {
        setError(t('creditEdit.errorValidBalance'));
        return;
      }

      const currentBalanceValue = currentBalance || 0;
      const difference = newBalanceValue - currentBalanceValue;

      // Check if there's actually a change
      if (Math.abs(difference) < 0.01) {
        setError(t('creditEdit.errorDifferentBalance'));
        return;
      }
    }

    if (!notes.trim()) {
      setError(t('creditEdit.errorExplainChange'));
      return;
    }

    if (!userMessage.trim()) {
      setError(t('creditEdit.errorUserMessage'));
      return;
    }

    if (!entryDate) {
      setError(t('creditEdit.errorSelectDate'));
      return;
    }

    if (isAmountMode && !source.trim()) {
      setError(t('creditEntry.errorProvideSource'));
      return;
    }

    setLoading(true);
    setError(null);

    try {
      // Use fiscal year as fallback if year prop is not provided
      let yearToUse = year;
      if (!yearToUse) {
        const fiscalYearStartMonth = selectedClient?.configuration?.fiscalYearStartMonth || 1;
        yearToUse = getFiscalYear(getMexicoDate(), fiscalYearStartMonth);
      }
      
      if (isAmountMode) {
        // For Add/Remove: Use direct history entry API with the amount
        const amountValue = parseDecimalInput(amount);
        // For remove mode, make amount negative
        const amountToAdd = mode === 'remove' ? -amountValue : amountValue;
        
        console.log('[CREDIT EDIT MODAL] Calling addCreditHistoryEntry API:', {
          clientId: selectedClient.id,
          unitId,
          amount: amountToAdd,
          date: entryDate,
          notes: notes.trim(),
          mode
        });
        
        // Convert date to ISO string format if needed
        let dateISO = entryDate;
        if (entryDate && !entryDate.includes('T')) {
          // If it's just a date (YYYY-MM-DD), convert to ISO string
          dateISO = new Date(entryDate + 'T00:00:00').toISOString();
        } else if (entryDate) {
          dateISO = new Date(entryDate).toISOString();
        }
        
        await addCreditHistoryEntry(
          selectedClient.id,
          unitId,
          amountToAdd, // Positive for add, negative for remove
          dateISO,
          notes.trim(),
          source.trim(),
          userMessage.trim()
        );
      } else {
        // For Edit Balance: Use updateCreditBalance API with new balance
        console.log('[CREDIT EDIT MODAL] Calling updateCreditBalance API:', {
          clientId: selectedClient.id,
          unitId,
          year: yearToUse,
          newBalanceValue,
          currentBalance
        });
        
        await updateCreditBalance(
          selectedClient.id,
          unitId,
          yearToUse,
          newBalanceValue,
          notes.trim(),
          entryDate, // Pass date for history entry
          userMessage.trim()
        );
      }

      // Call the parent's update callback if provided (this will refresh the data)
      if (onUpdate) {
        await onUpdate();
      }

      onClose();
    } catch (err) {
      console.error('Error updating credit balance:', err);
      setError(err.message || t('creditEdit.errorUpdateFailed'));
    } finally {
      setLoading(false);
    }
  };

  const handleCancel = () => {
    setNewBalance('');
    setAmount('');
    setEntryDate(getMexicoDateString());
    setNotes('');
    setUserMessage('');
    setSource('admin');
    userMessageTouchedRef.current = false;
    setError(null);
    onClose();
  };

  // Calculate preview values based on mode
  let newBalanceValue, difference, isAdd, isRemove;
  
  if (isAmountMode) {
    const amountValue = parseDecimalInput(amount) || 0;
    const currentBalanceValue = currentBalance || 0;
    if (mode === 'add') {
      newBalanceValue = currentBalanceValue + amountValue;
      difference = amountValue;
      isAdd = true;
      isRemove = false;
    } else { // mode === 'remove'
      newBalanceValue = currentBalanceValue - amountValue;
      difference = -amountValue;
      isAdd = false;
      isRemove = true;
    }
  } else {
    newBalanceValue = parseDecimalInput(newBalance) || currentBalance || 0;
    const currentBalanceValue = currentBalance || 0;
    difference = newBalanceValue - currentBalanceValue;
    isAdd = difference > 0;
    isRemove = difference < 0;
  }

  if (!isOpen) {
    return null;
  }

  if (!canEdit) {
    return (
      <div className="sandyland-modal-overlay" onClick={onClose}>
        <div className="sandyland-modal credit-balance-modal" onClick={(e) => e.stopPropagation()}>
          <div className="sandyland-modal-header credit-balance-modal-header">
            <h2 className="sandyland-modal-title credit-balance-modal-title">{t('creditEdit.accessDenied')}</h2>
            <button type="button" className="credit-modal-close" onClick={onClose} aria-label={t('creditHistory.close')}>×</button>
          </div>
          <div className="sandyland-modal-content credit-balance-modal-content">
            <p>{t('creditEdit.noPermission')}</p>
            <p>{t('creditEdit.adminOnly')}</p>
          </div>
          <div className="sandyland-modal-buttons">
            <button type="button" className="sandyland-btn sandyland-btn-secondary" onClick={onClose}>
              {t('creditHistory.close')}
            </button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="sandyland-modal-overlay" onClick={handleCancel}>
      <div className="sandyland-modal credit-balance-modal" onClick={(e) => e.stopPropagation()}>
        <div className={`sandyland-modal-header credit-balance-modal-header ${isAdd ? 'header-add' : isRemove ? 'header-remove' : ''}`}>
          <h2 className="sandyland-modal-title credit-balance-modal-title">
            {mode === 'add'
              ? `➕ ${t('creditEdit.title.add', { unitId })}`
              : mode === 'remove'
                ? `➖ ${t('creditEdit.title.remove', { unitId })}`
                : t('creditEdit.title.edit', { unitId })}
          </h2>
          <button type="button" className="credit-modal-close" onClick={handleCancel} aria-label={t('creditEdit.cancel')}>×</button>
        </div>
        
        <div className="sandyland-modal-content credit-balance-modal-content">
          {error && (
            <div className="credit-balance-error" role="alert">
              {error}
            </div>
          )}
          
          <div className="credit-balance-form-group">
            <label htmlFor="currentBalance">{t('creditEdit.currentBalance')}</label>
            <div className="credit-balance-current-value" id="currentBalance">
              {formatAsMXN(currentBalance || 0)}
            </div>
          </div>

          <div className="credit-balance-form-group">
            <label htmlFor="entryDate">{t('creditEdit.entryDateRequired')}</label>
            <input
              type="date"
              id="entryDate"
              value={entryDate}
              onChange={(e) => setEntryDate(e.target.value)}
              disabled={loading}
              required
            />
            <div className="credit-balance-form-hint">
              {t('creditEdit.entryDateHint')}
            </div>
          </div>

          {isAmountMode ? (
            <>
              <div className="credit-balance-form-group">
                <label htmlFor="amount">
                  {mode === 'add' ? t('creditEdit.amountAddRequired') : t('creditEdit.amountRemoveRequired')}
                </label>
                <input
                  type="text"
                  inputMode="decimal"
                  id="amount"
                  className="credit-balance-amount-input"
                  value={amount}
                  onChange={(e) => handleDecimalAmountChange(e, setAmount)}
                  placeholder={mode === 'add' ? t('creditEdit.amountAddPlaceholder') : t('creditEdit.amountRemovePlaceholder')}
                  disabled={loading}
                  required
                  autoComplete="off"
                />
                <div className="credit-balance-form-hint">
                  {mode === 'add' 
                    ? t('creditEdit.amountAddHint')
                    : t('creditEdit.amountRemoveHint')}
                </div>
              </div>

              {amount && parseDecimalInput(amount) > 0 && (
                <div className={`credit-balance-preview ${isAdd ? 'preview-add' : 'preview-remove'}`}>
                  <div className="credit-balance-preview-label">{t('creditEdit.previewCurrent')}</div>
                  <div className="credit-balance-preview-amount">
                    {formatAsMXN(currentBalance || 0)}
                  </div>
                  <div className="credit-balance-preview-label">{t('creditEdit.previewChange')}</div>
                  <div className="credit-balance-preview-change">
                    {isAdd ? '+' : ''}{formatAsMXN(difference)}
                  </div>
                  <div className="credit-balance-preview-label">{t('creditEdit.previewNew')}</div>
                  <div className="credit-balance-preview-amount">
                    {formatAsMXN(newBalanceValue)}
                  </div>
                </div>
              )}

              <div className="credit-balance-form-group">
                <label htmlFor="source">{t('creditEntry.sourceRequired')}</label>
                <select
                  id="source"
                  value={source}
                  onChange={(e) => setSource(e.target.value)}
                  disabled={loading}
                  required
                >
                  <option value="admin">admin</option>
                  <option value="correction">correction</option>
                  <option value="reconciliation">reconciliation</option>
                  <option value="unifiedPayment">unifiedPayment</option>
                  <option value="hoaDues">hoaDues</option>
                  <option value="waterBills">waterBills</option>
                  <option value="running_balance_computation">running_balance_computation</option>
                </select>
              </div>
            </>
          ) : (
            <>
              <div className="credit-balance-form-group">
                <label htmlFor="newBalance">{t('creditEdit.newBalanceRequired')}</label>
                <input
                  type="text"
                  inputMode="decimal"
                  id="newBalance"
                  className="credit-balance-amount-input"
                  value={newBalance}
                  onChange={(e) => handleDecimalAmountChange(e, setNewBalance, { allowNegative: true })}
                  placeholder={t('creditEdit.newBalancePlaceholder')}
                  disabled={loading}
                  required
                  autoComplete="off"
                />
                <div className="credit-balance-form-hint">
                  {t('creditEdit.newBalanceHint')}
                </div>
              </div>

              {Math.abs(difference) > 0.01 && (
                <div className={`credit-balance-preview ${isAdd ? 'preview-add' : 'preview-remove'}`}>
                  <div className="credit-balance-preview-label">{t('creditEdit.previewChange')}</div>
                  <div className="credit-balance-preview-change">
                    {isAdd ? '+' : ''}{formatAsMXN(difference)}
                  </div>
                  <div className="credit-balance-preview-label">{t('creditEdit.previewNew')}</div>
                  <div className="credit-balance-preview-amount">
                    {formatAsMXN(newBalanceValue)}
                  </div>
                </div>
              )}
            </>
          )}

          <div className="credit-balance-field-pair">
            <div className="credit-balance-form-group">
              <label htmlFor="notes">{t('creditEdit.notesRequired')}</label>
              <textarea
                id="notes"
                value={notes}
                onChange={(e) => setNotes(e.target.value)}
                placeholder={t('creditEdit.notesPlaceholder')}
                rows="3"
                disabled={loading}
                required
              />
              <div className="credit-balance-form-hint">
                {t('creditEdit.notesAuditHint')}
              </div>
            </div>

            <div className="credit-balance-form-group">
              <label htmlFor="userMessage">{t('creditEdit.userMessageRequired')}</label>
              <textarea
                id="userMessage"
                value={userMessage}
                onChange={(e) => {
                  userMessageTouchedRef.current = true;
                  setUserMessage(e.target.value);
                }}
                placeholder={t('creditEdit.userMessagePlaceholder')}
                rows="2"
                disabled={loading}
                required
              />
              <div className="credit-balance-form-hint">
                {t('creditEdit.userMessageHint')}
              </div>
            </div>
          </div>

          {!isAmountMode && (
            <div className="credit-balance-form-hint">
              {t('creditEdit.adminSourceHint')}
            </div>
          )}

          <div className="credit-balance-form-note">
            <strong>{t('creditEdit.noteTitle')}</strong> {t('creditEdit.noteBody')}
          </div>
        </div>

        <div className="sandyland-modal-buttons">
          <button 
            type="button"
            className="sandyland-btn sandyland-btn-secondary" 
            onClick={handleCancel}
            disabled={loading}
          >
            {t('creditEdit.cancel')}
          </button>
          <button 
            type="button"
            className={`sandyland-btn sandyland-btn-primary ${isAdd ? 'sandyland-btn-success' : isRemove ? 'sandyland-btn-danger' : ''}`}
            onClick={handleSave}
            disabled={
              loading || 
              !notes.trim() ||
              !userMessage.trim() ||
              !entryDate || 
              (isAmountMode ? (!amount || parseDecimalInput(amount) <= 0) : (!newBalance || Math.abs(difference) < 0.01))
            }
          >
            {loading 
              ? t('creditEdit.saving')
              : mode === 'add' 
                ? t('creditHistory.addCredit')
                : mode === 'remove' 
                  ? t('creditHistory.removeCredit')
                  : t('creditEdit.saveChanges')}
          </button>
        </div>
      </div>
    </div>
  );
}

export default CreditBalanceEditModal;
