const FALLBACK_LANGUAGE = 'EN';

function interpolate(template, params) {
  return template.replace(/\{(\w+)\}/g, (_match, key) => {
    const value = params[key];
    return value == null ? '' : String(value);
  });
}

export function getMobileString(language, key, params = {}) {
  const langKey = language === 'ES' ? 'ES' : FALLBACK_LANGUAGE;
  const languageMap = MOBILE_STRINGS[langKey] || MOBILE_STRINGS[FALLBACK_LANGUAGE];
  const fallbackMap = MOBILE_STRINGS[FALLBACK_LANGUAGE];
  const value = languageMap[key] ?? fallbackMap[key] ?? key;
  if (typeof value !== 'string') return String(value);
  if (!value.includes('{')) return value;
  return interpolate(value, params);
}

